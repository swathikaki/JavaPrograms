package JavaSessions;

public class WhileLoopPgm {

	public static void main(String[] args) {
		
		int i=10;
		
		while(i>=0)
		{
			System.out.println(i);
			i--;
		}
		
		System.out.println("--------------");
		
		// Do While
		
		int p=10;
		do{
			System.out.println(p);
			p--;
		}
		while(p>=1);
		
		
		
	}

}
